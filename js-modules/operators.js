export const addition = (a, b) => {
    return a + b;
};

export const subtraction = (a, b) => {
    return a - b;
};

export const multiplication = (a, b) => {
    return a * b;
};

export const division = (a, b) => {
    return a / b;
};

export const convertToPercentage = (a) => {
    return a / 100;
};

export const remainder = (a, b) => {
    return a % b;
};
